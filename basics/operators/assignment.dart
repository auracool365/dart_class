void main() {
  // Assignment operation
  String userName = "Faith";
  print(userName);

  int age = 100;
  print(age);

  // Compound assignment
  age = age + 10;
  print(age);

  age += 50; // short hand syntax
  print(age);

  // decrement operators
  int x = 5;
  print(--x); // decrements before print out
  print(x--); // prints out before decrement
  print(x);

  // increment operators
  int y = 10;
  print(++y); // increments before print out
  print(y++); // increments after print out
  print(y);
}
