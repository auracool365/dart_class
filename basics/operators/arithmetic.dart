void main() {
  // Arithmetic operators(aka math operators): used to perform math operations

  int num1 = 8, num2 = 6;

  // Addition
  print('$num1 + $num2 = ${num1 + num2}');
  print(2 + 5);

  // Subtraction
  print('$num1 - $num2 = ${num1 - num2}');
  print(10 - 5);

  // Multiplication
  print('$num1 x $num2 = ${num1 * num2}');
  print(2 * 5);

  // Division
  print('$num1 / $num2 = ${num1 / num2}');
  print(10 / 5);

  // Integer division
  print('$num1 ~/ $num2 = ${num1 ~/ num2}');
  print(10 ~/ 5);

  // Modulus
  print('$num1 % $num2 = ${num1 % num2}');
  print(10 % 5);

  print("${11 % 2 == 0 ? "even" : "odd"}");
}
