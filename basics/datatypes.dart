void main() {
  // Number Types in dart: int, double, num
  // int
  int age = 969;
  int numberOfPlanets = 8;
  print(age);
  print(numberOfPlanets);

  // age = 99.9; // Error

  // double
  double pmsPrice = 1330.5;
  double accountBalance = 10_000_000;
  print(pmsPrice);
  print(accountBalance);

  // pmsPrice = 400;

  // num(umbrella type for int and double)
  num height = 6;
  print(height);

  height = 5.11;
  print(height);

  // String
  String firstName = "John";
  String lastName = "Doe";

  print(firstName);
  print(lastName);

  // bool
  bool isBroke = true;
  bool isTeenager = false;
  print(isBroke);
  print(isTeenager);

  // null
  String? test;
  print(test);
}
